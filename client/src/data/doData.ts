// Computed summary statistics
export function getSummaryStats(dos: DeliveryObligation[]) {
  const total = dos.length;
  const green = dos.filter(d => d.rag === "GREEN").length;
  const amber = dos.filter(d => d.rag === "AMBER").length;
  const red = dos.filter(d => d.rag === "RED").length;
  const complete = dos.filter(d => d.rag === "COMPLETE").length;
  const notStarted = dos.filter(d => d.rag === "NOT_STARTED").length;
  const totalPenaltyAtRisk = dos
    .filter(d => d.rag === "AMBER" || d.rag === "RED")
    .reduce((sum, d) => sum + d.penaltyExposureM, 0);
  const totalAllowance = dos.reduce((sum, d) => sum + d.allowanceM, 0);
  return { total, green, amber, red, complete, notStarted, totalPenaltyAtRisk, totalAllowance };
}
